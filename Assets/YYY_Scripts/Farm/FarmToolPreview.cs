                {
                    // è¾¹æ¡†åŒºåŸŸä¸ºç™½è‰²ï¼Œå†…éƒ¨é€æ˜
                    bool isBorder = x < border || x >= size - border || 
                                    y < border || y >= size - border;
                    pixels[y * size + x] = isBorder ? white : transparent;
                }
